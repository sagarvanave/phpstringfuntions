Converts the first character of a string to lowercase
<br><br><br>
ucfirst() - converts the first character of a string to uppercase<br><br>
ucwords() - converts the first character of each word in a string to uppercase<br><br>
strtoupper() - converts a string to uppercase<br><br>
strtolower() - converts a string to lowercase<br><br>
